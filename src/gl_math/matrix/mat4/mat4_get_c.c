#include <lite_gl.h>

t_vec4	c1_mat4(t_mat4 m)
{
	return (make_vec4(m[0][0], m[1][0], m[2][0], m[3][0]));
}

t_vec4	c2_mat4(t_mat4 m)
{
	return (make_vec4(m[0][1], m[1][1], m[2][1], m[3][1]));
}

t_vec4	c3_mat4(t_mat4 m)
{
	return (make_vec4(m[0][2], m[1][2], m[2][2], m[3][2]));
}

t_vec4	c4_mat4(t_mat4 m)
{
	return (make_vec4(m[0][3], m[1][3], m[2][3], m[3][3]));
}
