#include <lite_gl.h>

float	mat4_determinant(t_mat4 mat)
{
	float	det;

	det = \
		mat[0][0] * (mat[1][1] * (mat[2][2] * mat[3][3] - mat[2][3] * mat[3][2])
			- mat[1][2] * (mat[2][1] * mat[3][3] - mat[2][3] * mat[3][1])
			+ mat[1][3] * (mat[2][1] * mat[3][2] - mat[2][2] * mat[3][1]))
		- mat[0][1] * (mat[1][0]
			* (mat[2][2] * mat[3][3] - mat[2][3] * mat[3][2])
			- mat[1][2] * (mat[2][0] * mat[3][3] - mat[2][3] * mat[3][0])
			+ mat[1][3] * (mat[2][0] * mat[3][2] - mat[2][2] * mat[3][0]))
		+ mat[0][2] * (mat[1][0]
			* (mat[2][1] * mat[3][3] - mat[2][3] * mat[3][1])
			- mat[1][1] * (mat[2][0] * mat[3][3] - mat[2][3] * mat[3][0])
			+ mat[1][3] * (mat[2][0] * mat[3][1] - mat[2][1] * mat[3][0]))
		- mat[0][3] * (mat[1][0]
			* (mat[2][1] * mat[3][2] - mat[2][2] * mat[3][1])
			- mat[1][1] * (mat[2][0] * mat[3][2] - mat[2][2] * mat[3][0])
			+ mat[1][2] * (mat[2][0] * mat[3][1] - mat[2][1] * mat[3][0]));
	return (det);
}

float	mat3_determinant(t_mat3 mat)
{
	return (mat[0][0] * (mat[1][1] * mat[2][2] - mat[1][2] * mat[2][1])
		- mat[0][1] * (mat[1][0] * mat[2][2] - mat[1][2] * mat[2][0])
		+ mat[0][2] * (mat[1][0] * mat[2][1] - mat[1][1] * mat[2][0]));
}
