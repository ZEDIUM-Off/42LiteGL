#include <lite_gl.h>

t_vec3	c1_mat3(t_mat3 m)
{
	return (make_vec3(m[0][0], m[1][0], m[2][0]));
}

t_vec3	c2_mat3(t_mat3 m)
{
	return (make_vec3(m[0][1], m[1][1], m[2][1]));
}

t_vec3	c3_mat3(t_mat3 m)
{
	return (make_vec3(m[0][2], m[1][2], m[2][2]));
}
