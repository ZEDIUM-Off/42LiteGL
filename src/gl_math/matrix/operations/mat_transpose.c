#include <lite_gl.h>

void	mat4_transpose(t_mat4 res, t_mat4 mat)
{
	int	i;
	int	j;

	i = -1;
	while (++i < 4)
	{
		j = -1;
		while (++j < 4)
			res[i][j] = mat[j][i];
	}
}
